package com.exercicio3Fatorial.model;

public class Fatorial {
    int valor;
    String fatorial;

    public Fatorial(int valor) {
        this.valor = valor;
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }
    public String getFatorial() {
        return fatorial;
    }
    public void setFatorial(String fatorial) {
        this.fatorial = fatorial;
    }

}
